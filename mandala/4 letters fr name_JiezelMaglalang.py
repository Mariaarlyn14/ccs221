import turtle

turtle.setup(1000,1000)
j = turtle.Turtle()
j.screen.bgcolor("black")
j.shape("arrow")
j.pensize(20)
j.speed(5)

# letter J
j.color("red")
j.up()
j.backward(300)
j.down()
j.forward(130)
j.up()
j.backward(30)
j.down()
j.right(90)
j.forward(150)

turtle.tracer(False)
for x in range(180):
    j.forward(1)
    j.right(1)
turtle.tracer(True)

j.up()
j.forward(150)
j.right(90)
j.forward(200)

# letter I
j.color("orange")
j.down()
j.forward(100)
j.backward(50)
j.right(90)
j.forward(200)
j.right(90)
j.forward(50)
j.right(180)
j.forward(100)
j.up()
j.forward(60)
j.left(90)
j.forward(200)

# letter E
j.color("yellow")
j.down()
j.left(180)
j.forward(180)
j.left(180)
j.forward(180)
j.right(90)
j.forward(90)
j.left(180)
j.forward(90)
j.left(90)
j.forward(80)
j.left(90)
j.forward(70)
j.left(180)
j.forward(70)
j.left(90)
j.forward(120)
j.left(90)
j.forward(90)

# letter Z
j.color("green")
j.up()
j.forward(50)
j.left(90)
j.forward(200)
j.right(90)
j.down()
j.forward(100)
j.right(120)
j.forward(230)
j.left(120)
j.forward(100)
turtle.done()
